def build_system(section: str, sub_section: Optional[str] = None) -> str:
    """section/subSection 에 맞는 SYSTEM 프롬프트를 조립한다.

    미지원 section 값 검증은 핸들러 책임 — 여기서는 SUBJECT 외 전부 창체로 취급하고,
    미지정 subSection 은 T2 기본 지침으로 폴백한다.
    """
    if section == "SUBJECT":
        return SYSTEM_BASE + _SUBJECT_GUIDE + _FEWSHOT_SUBJECT
    guide = _EXTRACURRICULAR_GUIDE.format(
        label=_EXTRA_LABELS.get(sub_section, "창체"),
        template_note=_EXTRA_TEMPLATE_NOTES.get(sub_section, _EXTRA_DEFAULT_NOTE),
    )
    return SYSTEM_BASE + guide + _FEWSHOT_EXTRACURRICULAR


